public class Kota {
    private String idKota;
    private String namaKota;

    public Kota(String idKota, String namaKota) {
        this.idKota = idKota;
        this.namaKota = namaKota;
    }

    public String getIdKota() {
        return idKota;
    }

    public String getNamaKota() {
        return namaKota;
    }
}